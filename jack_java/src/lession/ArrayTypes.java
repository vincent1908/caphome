public class ArrayTypes {

	public static void main(String[] args) {
		
		int myarray [] = {1,2,3,4,5};
		
		System.out.println(myarray[3]);
		
		/*
		 int [] myarray = new int[3];
		 int myarray[] = new int[]{1,2,3};
		 */

		int index = 0;
		while(index<5){
			System.out.println(myarray[index]);
			index++;
		}
		
	}

}
