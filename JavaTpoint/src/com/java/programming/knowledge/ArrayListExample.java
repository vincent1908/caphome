package com.java.programming.knowledge;

import java.util.ArrayList;

public class ArrayListExample {
	
	public static void main(String[] args) {

		ArrayList <Integer> my_list = new ArrayList<Integer>(5);
		
		my_list.add(1);
		my_list.add(2);
		my_list.add(3);
		my_list.add(4);
		
		for(Integer x : my_list )
	System.out.println(x);
		
		System.out.println("size of array is : " +my_list.size());
		
		my_list.set(0, 100);
		
		
		for(Integer x : my_list )
	System.out.println(x);
		
		System.out.println("size of array is : " +my_list.size());

		
		my_list.remove(2);
		
		for(Integer x : my_list )
	System.out.println(x);
		
		System.out.println("size of array is : " +my_list.size());
		
		
		my_list.clear();
		
		
		for(Integer x : my_list )
	System.out.println(x);
		
		System.out.println("size of array is : " +my_list.size());

		
		
	}

}
