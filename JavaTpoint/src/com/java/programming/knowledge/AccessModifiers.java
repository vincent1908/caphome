package com.java.programming.knowledge;

public class AccessModifiers {

/*

*access level   class package subclass world
*public           Y      Y       Y       Y
*protected        Y      Y       Y       N
*no modifier      Y      Y       N       N
*private          Y      N       N       N
*/

	private int id;
	String name;
	

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
