package lession;

public class SwitchStatement {

	public static void main(String[] args) {

		
	int score = 0;
	
	switch (score){
	
	case 90:
		System.out.println("very good");
		break;
	
	case 60:
		System.out.println("good");
		break;
	
	case 40:
		System.out.println("average");
		break;
		
	case 20:
		System.out.println("poor");
		break;
		
	default:
		System.out.println("grade not defined");
		
	}
	

	}

}
