package objectClass;

public class ObjCls4 {

	// multiple objects
	
	int length;
	int width;
	
	 void insert(int l,int w){  
		  length=l;  
		  width=w;  
		 } 
	 
	 void calculateArea(){System.out.println(length*width);}  

	
	public static void main(String[] args) {

		ObjCls4 r1 = new ObjCls4(), r2 = new ObjCls4();
		
		r1.insert(10, 20);
		r1.calculateArea();
		
		r2.insert(20, 30);
		r2.calculateArea();
		
		
	}

}
